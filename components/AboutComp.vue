    <h3>Quel est le profil et l'histoire de votre entreprise ?</h3>
    <p>FraichUp est une entreprise créée en mai 2018, spécialisée dans la transformation de pommes de terre en frites fraîches prêtes à l'emploi pour la restauration. Plus précisément, nous lavons les pommes de terre, nous les épluchons, les coupons et les mettons sous vide avant de les expédier à nos clients restaurateurs, de l'exploitant de snack au chef semi-gastronomique. Pour ce faire, nous disposons d'un atelier semi-automatisé doté d'une éplucheuse, d'une machine à découper les légumes et d'une calibreuse. Nous adressons le monde du CHR (Café, Hôtels, Restaurants) azuréen, pour l'heure dans une zone géographique restreinte située entre Nice et Biot, à hauteur d'environ 5 400 kg de frites fraîches par semaine. L'entreprise, qui emploie trois personnes, a réalisé un chiffre d'affaires d'environ 200 000 euros lors de son premier exercice.</p>    

    <h3>Quelle est sa valeur ajoutée ?</h3>
    <p>Nous apportons aux restaurateurs une économie de charge et de la qualité. Il s'agit de contribuer à éviter le recours aux frites surgelés, chères à l'achat, dans un contexte où le fait maison et la question de l'authenticité des produits reviennent sur le devant de la scène. Nous leur faisons ainsi gagner du temps, de l'argent et de la qualité. Celle-ci se décline au niveau des produits. Nous nous fournissons auprès de producteurs français (Hauts de France) et italiens en fonction des saisons dans le but de nous adapter à l'évolution de la pomme de terre au cours de l'année et de proposer la variété la mieux “fritable” du moment. La qualité se décline aussi au niveau du service, qui est de proximité, avec par exemple un service dépannage au cas où le client serait en rupture de stock.</p>
    
    <h3>Quels sont vos priorités et besoins pour l'année qui vient ?</h3>
    <p>Nos besoins vont dépendre de la croissance de l'entreprise. Pour cela, nous avons mis en place une stratégie de développement sur la période d'avant-saison, de janvier à avril 2020, qui vise à augmenter notre portefeuille clients et notre chiffre d'affaires. Il s'agit donc d'étendre notre zone d'exploitation au secteur ouest du département, de faire appel à des commerciaux payés à la commission, d'investir dans un deuxième camion de livraison ainsi que dans une nouvelle machine pour produire plus. L'objectif étant de nous permettre de recruter pour la saison un employé d'aide à la prod et un livreur supplémentaires. Nous souhaitons également travailler notre visibilité avec le lancement, en 2020, d'un site internet vitrine et d'un compte Instagram.</p>    

